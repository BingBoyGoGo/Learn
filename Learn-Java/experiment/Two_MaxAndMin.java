public class Two_MaxAndMin{
	public static void main(String[] args) {
		System.out.printf("int \t数值范围：%d ~ %d\n", Integer.MIN_VALUE, Integer.MAX_VALUE);
		System.out.printf("long \t数值范围：%d ~ %d\n", Long.MIN_VALUE, Long.MAX_VALUE);
		System.out.printf("short \t数值范围：%d ~ %d\n", Short.MIN_VALUE, Short.MAX_VALUE);
		System.out.printf("byte \t数值范围：%d ~ %d\n", Byte.MIN_VALUE, Byte.MAX_VALUE);
		System.out.printf("float \t数值范围：%f ~ %f\n", Float.MIN_VALUE, Float.MAX_VALUE);
		System.out.printf("double \t数值范围：%f ~ %.5f\n", Double.MIN_VALUE, Double.MAX_VALUE);
	}
};